public class Arithmetic {
    public static void main(String[] args) {
        
        int a = 10;
        int b = 2;
        float sum = a+b;
        float diff = a-b;
        float mul = a*b;
        float div = a/b;
        float Module = a%b;
        System.out.println("sum is the:"+sum);
        System.out.println("diff is the:"+diff);
        System.out.println("Mul is the:"+mul);
        System.out.println("div is the:"+div);
        System.out.println("Module is the:"+Module);
        
    }
}
