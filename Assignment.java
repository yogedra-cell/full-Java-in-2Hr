public class Assignment {
    public static void main(String[] args) {
        
        // Initialize a variable 'number' with value 1
        int number = 1;
        // Post-increment: increases value by 1 after reading
        number++;
        // Prints updated value of 'number' (which becomes 2)
        System.out.println(number);
        
        // Initialize a variable 'a' with value 2
        int a = 2;
        // Post-increment: increases value by 1
        a++;
        // Prints updated value of 'a' (which becomes 3)
        System.out.println(a); 

        // Initialize a variable 'b' with value 10
        int b = 10;
        // Post-decrement: decreases value by 1
        b--;
        // Prints updated value of 'b' (which becomes 9)
        System.out.println(b);

        // Initialize a variable 'c' with value 0
        int c = 0;
        // Pre-decrement: decreases value by 1 before reading
        --c;
        // Prints updated value of 'c' (which becomes -1)
        System.out.println(c);

    }
}
