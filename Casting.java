public class Casting {
    public static void main(String[] args) {

        //Implcit Casting
        int price = 100;
        double fP = price +18.99;
        System.out.println(fP);

        // Explicit Casting
        float P = 100.9f;
        int MP = (int)P + 18;
        System.out.println(MP);



        
    }
    
}
